export const getExcelColumnName = (colIndex: number) => {
    let dividend = colIndex;
    let columnName = "";
    let modulo;

    while (dividend > 0) {
        modulo = (dividend - 1) % 26;
        columnName = String.fromCharCode(65 + modulo) + columnName;
        dividend = Math.floor((dividend - modulo) / 26);
    }

    return columnName;
}